
class Cereal(
        string name,
        string mfr,
        bool cold,
        int calories,
        int protein,
        int fat,
        int sodium,
        double fiber,
        double carbo,
        int sugars,
        int potass,
        int vitamins,
        int shelf,
        int weight,
        double cups,
        decimal rating)
{
    string Name {get;} = name;
    string MFR {get;} = mfr;
    bool Cold {get;} = cold;
    int Calories {get;} = calories;
    int Protein {get;} = protein; 
    int Fat {get;} = fat;
    int Sodium {get;} = sodium; 
    double Fiber {get;} = fiber; 
    double Carbohydrates {get;} = carbo;
    int Sugars {get;} = sugars;
    int Potass {get;} = potass; 
    int Vitamins {get;} = vitamins;
    int Shelf {get;} = shelf; 
    int Weight {get;} = weight;
    double Cups {get;} = cups;
    decimal Rating {get;} = rating;
}
